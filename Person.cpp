#include <iostream>
#include <string>
#include "Person.hpp"
using std::string;

/********************************************************************************************************************
 **                      Person::Person()
 ** This default constructor initializes the name to "" and the age to 0.
 *******************************************************************************************************************/

Person::Person()
{
    name = "";
    age = 0;
}

/*******************************************************************************************************************
 **                      Person::Person(string, int)
 ** This constructor takes in two parameters and uses them to initialize the data members.
 ********************************************************************************************************************/

Person::Person(string nameIn, int ageIn)
{
    name = nameIn;
    age = ageIn;
}

/********************************************************************************************************************
 **                      Person::getName
 ** This function returns the value in member variable name.
 ********************************************************************************************************************/ 

string Person::getName()
{
    return name;
}

/********************************************************************************************************************
 **                      Player::getAge
 ** This function returns the value in member variable age.
 ********************************************************************************************************************/

double Person::getAge()
{
    return age;
}
